package book.array;

import java.util.*;

public class Subject {
			
	private String subName;
	private int score;
	
	
	public Subject (String subName, int score){
		this.subName = subName;
		this.score = score;
		
		
	}
	
	public Subject () {}
	

	public String getSubName() {
		return subName;
	}


	public void setSubName(String subName) {
		this.subName = subName;
	}


	public int getScore() {
		return score;
	}


	public void setScore(int score) {
		this.score = score;
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
}
