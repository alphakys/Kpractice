package baekjun;

import java.util.Scanner;

public class While10952 {

	public static void main(String[] args) {
		int A,B;
		
		
		
		Scanner sc = new Scanner(System.in);
		
		 A=sc.nextInt();
		 B=sc.nextInt();
		
		while(A!=0 && B!=0) {
			System.out.println(A+B);
			 A=sc.nextInt();
			 B=sc.nextInt();
		} return;
			
		
	
	}

}
